export const optional = true;

export function parseDotNetShortAssemblyQualifiedName(assemblyQualifiedName) {
    //* expect shortened .NET fully qualified assembly name e.g. Soap.Api.Messages.MessageA, Soap.Api.Messages
    const messageNameMatches = assemblyQualifiedName.match(/^(.+),.+$/i);
    const assemblyNameMatches = assemblyQualifiedName.match(/^.+, (.+)$/i)
    const className = messageNameMatches[1].replace(/\+/g, '.');
    const assemblyName = assemblyNameMatches[1];
    return {className, assemblyName};
}

export function getHeader(message, headerKey) {
    const header = _.find(message.headers, h => h.key == headerKey);
    if (!header) throw `header ${headerKey} not defined`;
    return header.value;
}


export function setHeader(command, headerKey, value) {
    _.remove(command.headers, h => h.key == headerKey);
    command.headers.push(makeHeader(headerKey, value));

    function makeHeader(name, value) {
        return {key: name, value, active: true};
    }
}

export function validateArgs(...requiredArgs) {
    
    const arrayOfErrors = [];
    
    requiredArgs.forEach(arg => {
        try {
            const argName = Object.keys(arg[0])[0]; //- get first key of 1st element which is an object 
            const argValue = Object.values(arg[0])[0]; //- get first value of 1st element which is an object
            let requiredType = arg[1]; //- get 2nd element (type)
            let isOptional = !!arg[2]; //- get 3rd element (optional)
            
            const requireArrayOf = Array.isArray(requiredType);
            if (requireArrayOf === true) {
                if (Array.isArray(argValue) === false)
                    throw `Argument ${argName} is supposed to be an array but the value provided is of type ${typeof argValue}. E^05`;
                requiredType = requiredType[0];
                argValue.forEach((v, i) => {
                    checkType(
                        `Argument ${argName}, Index ${i}`,
                        v,
                        requiredType,
                        isOptional,
                    );
                });
            } else {
                    checkType(`Argument ${argName}`, argValue, requiredType, isOptional);
            }
        } catch (err) {
            if (err.toString().match(/E\^/g)) {
                arrayOfErrors.push(err);
            } else {
                throw err;    
            }
        }
    });
    
    if (arrayOfErrors.length > 0) throw arrayOfErrors.join(', \r\n');

    function checkType(argName, argValue, requiredType, isOptional) {
        
        const requireClass = typeof requiredType === 'function';

        if (argValue === undefined) {
            if (isOptional) return;
            throw  `${argName} is required but was undefined E^01`;
        } else if (argValue === null) {
            if (isOptional) return;
            throw  `${argName} is required but was null. E^02`;
        } else if (requiredType === 'datetime') {
            if (!isISODateTime(argValue.toString())) {
                throw new Error(
                    `${argName} is expected to be an ISO Date Time formatted string E^06`,
                );
            }
        } else if (requiredType === 'guid') {
            if (!isGuid(argValue.toString()))
            throw new Error(
                `${argName} is expected to be a UUID/GUID formatted string E^07`,
            );
        }
         else if (requireClass) {
            if (!(argValue instanceof requiredType)) {
                throw new Error(
                    `${argName} was of class type ${typeof argValue}, expected class ${requiredType.name} E^03`,
                );
            }
        } else if (typeof argValue !== requiredType) {
            throw new Error(
                `${argName} was of type ${typeof argValue}, expected ${requiredType} E^04`,
            );
        }
         
        function isGuid(string) {
            return string.match(/^[0-9a-f]{8}-[0-9a-f]{4}-[0-5][0-9a-f]{3}-[089ab][0-9a-f]{3}-[0-9a-f]{12}$/i);
        }

        function isISODateTime(string) {
            //* https://stackoverflow.com/questions/3143070/javascript-regex-iso-datetime
            return string.match(/^(\d{4}-[01]\d-[0-3]\dT[0-2]\d:[0-5]\d:[0-5]\d\.\d+([+-][0-2]\d:[0-5]\d|Z))|(\d{4}-[01]\d-[0-3]\dT[0-2]\d:[0-5]\d:[0-5]\d([+-][0-2]\d:[0-5]\d|Z))|(\d{4}-[01]\d-[0-3]\dT[0-2]\d:[0-5]\d([+-][0-2]\d:[0-5]\d|Z))$/);
        }
    }
}

export const convertObjectNullPropertiesToUndefined = item => {
    if (Array.isArray(item)) {
        return item.map(arrayItem =>
            convertObjectNullPropertiesToUndefined(arrayItem),
        );
    }
    if (typeof item !== 'object' || item === null)
        return item === null ? '' : item;

    return Object.keys(item).reduce((newObject, key) => {
        const value = item[key];

        let newValue = item[key];

        if (typeof value === 'object' && value !== null) {
            newValue = convertObjectNullPropertiesToUndefined(value);
        }
        if (Array.isArray(value)) {
            newValue = value.map(arrayItem =>
                convertObjectNullPropertiesToUndefined(arrayItem),
            );
        }
        return {
            ...newObject,
            [key]: newValue === null ? '' : newValue,
        };
    }, {});
};

export function uuidv4() {
    if (!!window.crypto) {
        return ([1e7] + -1e3 + -4e3 + -8e3 + -1e11).replace(/[018]/g, c =>
            (
                c ^
                (crypto.getRandomValues(new Uint8Array(1))[0] & (15 >> (c / 4)))
            ).toString(16),
        );
    } else {
        return 'xxxxxxxx-xxxx-4xxx-yxxx-xxxxxxxxxxxx'.replace(/[xy]/g, function (c) {
            var r = (Math.random() * 16) | 0,
                v = c == 'x' ? r : (r & 0x3) | 0x8;
            return v.toString(16);
        });
    }
}

